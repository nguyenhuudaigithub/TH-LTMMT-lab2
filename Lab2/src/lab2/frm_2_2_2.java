    private void btnVietFileNhiPhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVietFileNhiPhanActionPerformed
        // TODO add your handling code here:
        byte a[] = new byte[20];
        File file;
        char s [] = txtNhapChuoi.getText().toCharArray();
        for (int i = 0; i < s.length; i++) {
            a[i] = (byte) s[i];
        }
        try {
            file = new File (txtFileName.getText());
            FileOutputStream fo = new FileOutputStream(file);
            fo.write(a);
            fo.close();
            JOptionPane.showMessageDialog(null, "Viet file nhi phan thanh cong!");
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_btnVietFileNhiPhanActionPerformed

    private void btnDocFileNhiPhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDocFileNhiPhanActionPerformed
        // TODO add your handling code here:
        byte a[];
        File file;
        try {
            file = new File(txtFileName.getText());
            FileInputStream fi = new FileInputStream(file);
            a=new byte[fi.available()];
            fi.read(a);
            fi.close();
            txtNoiDungFile.setText(new String (a));
            JOptionPane.showMessageDialog(null, "Doc file nhi phan thanh cong!");
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnDocFileNhiPhanActionPerformed

