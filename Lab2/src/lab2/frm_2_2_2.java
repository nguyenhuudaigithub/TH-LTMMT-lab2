    private void btnDocFileVanBanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDocFileVanBanActionPerformed
        // TODO add your handling code here:
        try {
            FileReader fr = new FileReader(new File(txtFileName.getText()));
            StringBuffer sb = new  StringBuffer();
            char ca[] = new char[5];
            while (fr.ready()) {
                int len = fr.read(ca);
                sb.append(ca,0,len);    
            }
            JOptionPane.showMessageDialog(null, "Doc file van ban thanh cong!");
            txtNoiDungFile.setText(sb+"");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnDocFileVanBanActionPerformed

